	FEAT_SOURCE_URL=http://ftp.gnu.org/gnu/autoconf/autoconf-2.69.tar.gz
	FEAT_SOURCE_URL_FILENAME=autoconf-2.69.tar.gz
	FEAT_SOURCE_URL_PROTOCOL=HTTP_ZIP


